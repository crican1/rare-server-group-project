#Defining a class for comments.
class Comment():

    # Class initializer. It has 5 custom parameters, with the
    # special `self` parameter that every method on a class
    # needs as the first parameter.

    def __init__(self, id, author_id, post_id, content, user_id,
                 first_name, last_name, email, bio, username, password, created_on, active):
        self.id = id
        self.author_id = author_id
        self.post_id = post_id
        self.content = content
        self.content = user_id
        self.content = first_name
        self.content = last_name
        self.content = email
        self.content = bio
        self.content = username
        self.content = password
        self.content = created_on
        self.content = active
